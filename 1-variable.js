// VARIABLE IS BASICALLY A BOX FOR HOLDING DATA. you use the "let" or "var" keyword to create variables in JavaScript

// VARIABLE INITIALIZATION
var firstName = 'Steven';
let secondName = 'Universe';
console.log(firstName);
console.log(secondName);

// VARIABLE DECLARATION
var thirdName;
let fourthName;
console.log();
console.log();

// VARIABLE ASSIGNMENT
//  "=" is the assignment operator
thirdName = 'Greg';
fourthName = 'Universe';
console.log();
console.log();
// THE VALUES OF VARIABLES CAN BE CHANGED BY ASSIGNNING A NEW VALUE
thirdName = 'Steven';
console.log();
// NOW THE VALUE OF THE VARIABLE "thirdName" HAS CHANGED FROM 'Greg' TO 'Steven'



